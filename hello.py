print("hello world") # print是一个输出函数，这是一段注释
print("这是一个字面常量") # 字面常量,包括数字和字符串
print("2") # 整数
print("52.3E-4") # 浮点数，E表示10的幂，即52.3 * 10^-4
print("这是双引号括起来的字符串")
print('这是单引号括起来的字符串')
print("""三个引号括起来的字符串
可以写多行字符串
就像这一段""") #多行字符串也可以永三个单引号括起来
# 转义字符：将有意义的符号转换为字符串
print('what \'s your name') # 转义字符\'
print('this is the first line\nthis is the second line') #转行符\n
print('123\t456\t789') #制表符\t
print('this is the first sentence.\
 this is the second sentence.') # 末尾\表示下一行继续
print(r'原始字符串\n\t') # 在字符串前使用r或者R，转义字符将会失效，即输出原始字符串
